package modelo;

public class PojoCiclos {

	private int id_C;
	private String descripcion;
	private String nombre;
	
	public PojoCiclos(int id_C, String descripcion, String nombre) {
		super();
		this.id_C = id_C;
		this.descripcion = descripcion;
		this.nombre = nombre;
	}

	public int getId_C() {
		return id_C;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public String getNombre() {
		return nombre;
	}
	
	
	
	
	
}
